                                <div class="timeline-body">
                                    <p class="text-muted">Em paralelo ao funcionamento do CASD Vestibulares, ainda com o objetivo de fazer da educação um papel de transformação social, três alunos de graduação do ITA fundaram, em 2010, o Casdinho. Inspirados na ONG Olímpicos de Santa Isabel (OSI), decidiram dar aulas preparatórias para o Colégio da Embraer - CEJW. Para tanto, firmou-se um compromisso de aulas com um grupo seleto de alunos da rede pública de ensino de São José dos Campos que participavam do Programa Decolar. No início do primeiro semestre eram apenas 15 alunos, quantidade que aumentou para 50 ainda no mesmo ano. No final de um ano de tanto esforço, os resultados não poderiam deixar de serem surpreendentes. Esses resultados inspiraram ainda mais os alunos do ITA que abraçavam a causa e um número maior de membros permitiu que o Casdinho proporcionasse no seu segundo ano uma oportunidade de ensino para mais estudantes. Selecionaram-se, pelo primeiro Vestibulinho (Processo Seletivo do Casdinho), os 120 mais bem preparados alunos do ensino fundamental de escolas públicas da região do Vale do Paraíba. Nesse ano também começaram as aulas preparatórias para Olimpíadas, uma oportunidade que os alunos tiveram de desenvolver ainda mais o seu potencial.
 
									No ano de 2012, decidiu-se continuar o plano de expansão do Casdinho: atender 140 alunos desta vez. E não somente os mais bem preparados dos 7º e 8º anos das escolas públicas, mas dessa vez com um processo seletivo com duas etapas: o Vestibulinho (a nossa prova) e uma entrevista de renda, selecionando os mais carentes entre os primeiros colocados no Vestibulinho. 

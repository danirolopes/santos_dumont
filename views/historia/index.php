                                    <p class="text-muted">Mesmo com poucos anos de existência, o CASD Vestibulares cresceu muito. No ano de 2005, o corpo administrativo do curso resolveu ousar. Foi construída uma sede própria para atender mais alunos carentes e garantir uma maior sustentabilidade e qualidade do curso. O projeto foi realizado com sucesso, por meio de doações de instituições públicas e privadas, entre eles: Instituto Embraer, Petrobras, Banco Santander e Prefeitura Municipal de São José dos Campos. Em 2006, foi inaugurada a sede-própria e o número de alunos atendidos anualmente, que em 1999 era 100, aumentou para mais de 500 alunos, solidificando o papel transformador do CASD Vestibulares perante a sociedade joseense. </p>

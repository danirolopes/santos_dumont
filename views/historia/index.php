                                </div>
                                <div class="timeline-body">
                                    <p class="text-muted">Em 1997, alunos de graduação do ITA decidiram reativar o curso, agora com o nome CASD Vestibulares - Curso Alberto Santos Dumont, apoiados pelo prefeito de São José dos Campos na época, Emanuel Fernandes, ex-aluno e ex-professor do CASD e formado em engenharia aeronáutica pelo ITA. A partir de então, o CASD Vestibulares, inicialmente com 60 alunos, passou a apresentar um novo enfoque educacional, não mais voltado para o setor comercial. A nova proposta do curso, essencialmente filantrópica, visava a atender à população carente da cidade de São José dos Campos e regiões vizinhas, oferecendo ensino de qualidade àqueles que não tinham condições de arcar com os custos de um pré-vestibular comercial. Em outubro de 1998, o CASD Vestibulares teve sua pessoa jurídica definida como uma Associação Civil sem fins lucrativos. </p>
